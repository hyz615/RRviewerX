  const KEY = 'lang';
  function sysDefaultLang(){
    try{
      const cand = (navigator.language || (navigator.languages && navigator.languages[0]) || document.documentElement.lang || '').toLowerCase();
      return cand.startsWith('zh') ? 'zh' : 'en';
    }catch(_){ return 'zh'; }
  }
  function getLang(){
    const hash = (location.hash||'').toLowerCase();
    if (hash.includes('lang=en')) return 'en';
    if (hash.includes('lang=zh')) return 'zh';
    try {
      const saved = localStorage.getItem(KEY);
      if (saved === 'en' || saved === 'zh') return saved;
    } catch(e){}
    return sysDefaultLang();
  }
  function setLang(l){ try { localStorage.setItem(KEY, l); } catch(e){} }
  function t(key){
    const l = getLang();
    return (dict[l] && dict[l][key]) || (dict.zh && dict.zh[key]) || key;
  }
